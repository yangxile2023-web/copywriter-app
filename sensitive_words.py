def check_sensitive_words(text):
    """检查文本中的敏感词，返回找到的敏感词列表"""
    if not text:
        return []
    
    found = []
    text_str = str(text)
    
    for word in SENSITIVE_WORDS:
        if word in text_str:
            found.append(word)
    
    return list(set(found))  # 去重
